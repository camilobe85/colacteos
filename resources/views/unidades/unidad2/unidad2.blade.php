            <section id="2.2." class="NavigationItem"  title="Sección 2.2.">
            <h3 class="subtUnidad">2.2. Principios de la economía solidaria </h3>
            <ul>
            <li><span >El ser humano, su trabajo y los mecanismos de cooperaci&oacute;n tienen primac&iacute;a sobre los medios de producci&oacute;n. </span></li>
            <li><span >Esp&iacute;ritu de solidaridad, cooperaci&oacute;n, participaci&oacute;n y ayuda mutua. </span></li>
            <li><span >Administraci&oacute;n democr&aacute;tica, participativa, autogestionaria y emprendedora.</span></li>
            <li><span >Adhesi&oacute;n voluntaria, responsable y abierta.</span></li>
            <li><span >Propiedad asociativa y solidaria sobre los medios de producci&oacute;n.</span></li>
            <li><span >Participaci&oacute;n econ&oacute;mica de los asociados en justicia y equidad. </span></li>
            <li><span >Formaci&oacute;n e informaci&oacute;n para sus miembros, de manera permanente, oportuna y progresiva. </span></li>
            <li><span >Autonom&iacute;a, autodeterminaci&oacute;n y autogobierno. </span></li>
            <li><span >Servicio a la comunidad. </span></li>
            <li><span >Integraci&oacute;n con otras organizaciones del mismo sector. </span></li>
            <li><span >Promoci&oacute;n de la cultura ecol&oacute;gica.</span></li>
            </ul>
            </section>
            <!--2.3.-->
            <section id="2.3." class="NavigationItem"  title="Sección 2.3.">
            <h3 class="subtUnidad">2.3. Fines de la Economía Solidaria </h3>
            <p><span><span >La ley colombiana establece los fines que debe perseguir la econom&iacute;a solidaria, los cuales est&aacute;n establecidos en la Ley 454 de 1998 en su art&iacute;culo 5: </span></span></p>
            <ul>
            <li><span><span >Promover el desarrollo integral del ser humano. </span></span></li>
            <li><span><span >Generar pr&aacute;cticas que consoliden una corriente vivencial de pensamiento solidario, cr&iacute;tico, creativo y emprendedor como medio para alcanzar el desarrollo y la paz de los pueblos. </span></span></li>
            <li><span><span >Contribuir al ejercicio y perfeccionamiento de la democracia participativa. </span></span></li>
            <li><span><span >Participar en el dise&ntilde;o y ejecuci&oacute;n de planes, programas y proyectos de desarrollo econ&oacute;mico y social. </span></span></li>
            <li><span><span >Garantizar a sus miembros la participaci&oacute;n y acceso a la formaci&oacute;n, el trabajo, la propiedad, la informaci&oacute;n, la gesti&oacute;n y distribuci&oacute;n equitativa de beneficios sin discriminaci&oacute;n alguna.</span></span></li>
            </ul>
            </section>
             <!--2.4.-->
            <section id="2.4." class="NavigationItem"  title="Sección 2.4.">
            <h3 class="subtUnidad">2.4. Valores de la Economía Solidaria </h3>
            <ol>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Autoayuda o esfuerzo propio:</strong></span></span><span><span >&nbsp;El asociado no puede ser el sujeto pasivo de la acci&oacute;n ben&eacute;fica de otros, sino el participe activo para el mejoramiento de su propio destino.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Responsabilidad propia:</strong></span></span><span><span >&nbsp;Significa asumir las propias responsabilidades en la conducci&oacute;n de las cooperativas y no delegarlas a personas &oacute; personas ajenas.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Democracia:</strong></span></span><span><span >&nbsp;Direcci&oacute;n y administraci&oacute;n de la cooperativa en manos&nbsp;de sus asociados con base en la igualdad.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Igualdad:</strong></span></span><span><span >&nbsp;Como manifestaci&oacute;n de la dignidad de la persona humana por encima de otras consideraciones.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Equidad:</strong></span></span><span><span >&nbsp;Caracter&iacute;stica particular de la justicia que est&aacute; m&aacute;s all&aacute; de formalismos simplistas.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Solidaridad:</strong></span></span><span><span >&nbsp;Caracter&iacute;stica sobresaliente del sistema cooperativo que se manifiesta cuando los esfuerzos individuales hacia el objetivo com&uacute;n se entrelazan estrechamente para configurar un esfuerzo colectivo que supone la participaci&oacute;n de todos.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Honestidad y transparencia:</strong></span></span><span><span >&nbsp;Conceptos que vinculan convicciones y actividades relacionadas con honradez, decencia, franqueza, aplicadas a la empresa cooperativas en las finanzas, los precios, las pesas y medidas, la verdad en los informes y estados financieros.</span></span></li>
            <li><span><span ><strong>&nbsp;&nbsp;&nbsp;&nbsp;Responsabilidad y vocaci&oacute;n social:</strong></span></span><span><span >&nbsp;Valores que hacen referencia a la presencia activa de las cooperativas y sus asociados en las comunidades donde act&uacute;a con el fin de contribuir a su mejoramiento integral o desarrollo humano sostenible.</span></span></li>
            </ol>
            </section>
