            <!--2.1.-->
            <h2 class="subtUnidad">2.1. ¿Qué se entiende por economía solidaria?</h2>
            <p class="western" align="justify"><span><span style="font-size: medium;">La econom&iacute;a solidaria es una forma diferente de hacer econom&iacute;a, cuya caracter&iacute;stica primordial es satisfacer las necesidades del ser humano. &Eacute;sta se conforma por un conjunto de fuerzas sociales organizadas en formas asociativas, identificadas por pr&aacute;cticas democr&aacute;ticas, autogestionarias, humanistas y sin &aacute;nimo de lucro; esto quiere decir de acuerdo a la legislaci&oacute;n Colombiana del Sector Solidario. Ley 454 de 1994: </span></span></p>
            
            <div class="alert alert-success" role="alert">
                <blockquote>
                <p><em>"Ésta se conforma por un conjunto de fuerzas sociales organizadas en formas asociativas…"</em></p>
                </blockquote>
                <p>La ley no define la economía solidaria como un conjunto de empresas sino como una manifestación social organizada asociativamente. Los tipos de asociación dentro de la Economía Solidaria en Colombia se abordarán más adelante en este curso.</p>
            </div>
            <div class="alert alert-info" role="alert">
                <blockquote>
                <p><em>"…identificadas por prácticas,…"</em></p>
                </blockquote>
                <p>La ley identifica a las organizaciones solidarias a partir de ciertas características que no tienen las empresas capitalistas. “…autogestionarias…”</p>
            </div>
            <div class="alert alert-warning" role="alert">
                <blockquote>
                <p><em>"…democráticas,…"</em></p>
                </blockquote>
                <p>Estas formas asociativas deben ser democráticas, es decir que las decisiones las toman todos los asociados bajo las mismas condiciones.</p>
            </div>
            <div class="alert alert-danger" role="alert">
                <blockquote>
                <p><em>“…autogestionarias,…”</em></p>
                </blockquote>
                <p>La autogestión implica autonomía para determinar las fuentes de financiación, la forma de distribuir los excedentes y la toma de decisiones de las empresas.</p>
            </div>
            <div class="alert alert-success" role="alert">
                <blockquote>
                <p><em>“…humanistas…”</em></p>
                </blockquote>
                <p>Las prácticas desarrolladas por estas organizaciones deben centrase en el ser humano y buscar el desarrollo integral del mismo. Estas empresas basan su acción administrativa en considerar al ser humano como sujeto, actor y fin de la economía, es decir, que el principal objetivo de las organizaciones solidarias no es generar dinero, sino lograr un desarrollo integral de las personas.</p>
            </div>
            <div class="alert alert-info" role="alert">
                <blockquote>
                <p><em>“…y sin ánimo de lucro.”</em></p>
                </blockquote>
                <p>La economía solidaria pretende generar prácticas que permitan a las empresas sostenerse e invertir el excedente de la producción en actividades y proyectos que mejoren la calidad de vida de los asociados, lo que quiere decir que se persigue un lucro colectivo.</p>
            </div>

            <!--2.2. -->
            <h3 class="subtUnidad">2.2. Principios de la economía solidaria </h3>
            <ul>
            <li class="western"><span >El ser humano, su trabajo y los mecanismos de cooperaci&oacute;n tienen primac&iacute;a sobre los medios de producci&oacute;n. </span></li>
            <li class="western"><span >Esp&iacute;ritu de solidaridad, cooperaci&oacute;n, participaci&oacute;n y ayuda mutua. </span></li>
            <li class="western"><span >Administraci&oacute;n democr&aacute;tica, participativa, autogestionaria y emprendedora.</span></li>
            <li class="western"><span >Adhesi&oacute;n voluntaria, responsable y abierta.</span></li>
            <li class="western"><span >Propiedad asociativa y solidaria sobre los medios de producci&oacute;n.</span></li>
            <li class="western"><span >Participaci&oacute;n econ&oacute;mica de los asociados en justicia y equidad. </span></li>
            <li class="western"><span >Formaci&oacute;n e informaci&oacute;n para sus miembros, de manera permanente, oportuna y progresiva. </span></li>
            <li class="western"><span >Autonom&iacute;a, autodeterminaci&oacute;n y autogobierno. </span></li>
            <li class="western"><span >Servicio a la comunidad. </span></li>
            <li class="western"><span >Integraci&oacute;n con otras organizaciones del mismo sector. </span></li>
            <li class="western"><span >Promoci&oacute;n de la cultura ecol&oacute;gica.</span></li>
            </ul>

            <!--2.3.-->
            <h3 class="subtUnidad">2.3. Fines de la Economía Solidaria </h3>
            <p class="western"><span><span style="font-size: medium;">La ley colombiana establece los fines que debe perseguir la econom&iacute;a solidaria, los cuales est&aacute;n establecidos en la Ley 454 de 1998 en su art&iacute;culo 5: </span></span></p>
            <ul>
            <li class="western"><span><span style="font-size: medium;">Promover el desarrollo integral del ser humano. </span></span></li>
            <li class="western"><span><span style="font-size: medium;">Generar pr&aacute;cticas que consoliden una corriente vivencial de pensamiento solidario, cr&iacute;tico, creativo y emprendedor como medio para alcanzar el desarrollo y la paz de los pueblos. </span></span></li>
            <li class="western"><span><span style="font-size: medium;">Contribuir al ejercicio y perfeccionamiento de la democracia participativa. </span></span></li>
            <li class="western"><span><span style="font-size: medium;">Participar en el dise&ntilde;o y ejecuci&oacute;n de planes, programas y proyectos de desarrollo econ&oacute;mico y social. </span></span></li>
            <li class="western"><span><span style="font-size: medium;">Garantizar a sus miembros la participaci&oacute;n y acceso a la formaci&oacute;n, el trabajo, la propiedad, la informaci&oacute;n, la gesti&oacute;n y distribuci&oacute;n equitativa de beneficios sin discriminaci&oacute;n alguna.</span></span></li>
            </ul>

             <!--2.4.-->
            <h3 class="subtUnidad">2.4. Valores de la Economía Solidaria </h3>
            <ol>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Autoayuda o esfuerzo propio:</strong></span></span><span><span style="font-size: medium;">&nbsp;El asociado no puede ser el sujeto pasivo de la acci&oacute;n ben&eacute;fica de otros, sino el participe activo para el mejoramiento de su propio destino.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Responsabilidad propia:</strong></span></span><span><span style="font-size: medium;">&nbsp;Significa asumir las propias responsabilidades en la conducci&oacute;n de las cooperativas y no delegarlas a personas &oacute; personas ajenas.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Democracia:</strong></span></span><span><span style="font-size: medium;">&nbsp;Direcci&oacute;n y administraci&oacute;n de la cooperativa en manos&nbsp;de sus asociados con base en la igualdad.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Igualdad:</strong></span></span><span><span style="font-size: medium;">&nbsp;Como manifestaci&oacute;n de la dignidad de la persona humana por encima de otras consideraciones.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Equidad:</strong></span></span><span><span style="font-size: medium;">&nbsp;Caracter&iacute;stica particular de la justicia que est&aacute; m&aacute;s all&aacute; de formalismos simplistas.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Solidaridad:</strong></span></span><span><span style="font-size: medium;">&nbsp;Caracter&iacute;stica sobresaliente del sistema cooperativo que se manifiesta cuando los esfuerzos individuales hacia el objetivo com&uacute;n se entrelazan estrechamente para configurar un esfuerzo colectivo que supone la participaci&oacute;n de todos.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Honestidad y transparencia:</strong></span></span><span><span style="font-size: medium;">&nbsp;Conceptos que vinculan convicciones y actividades relacionadas con honradez, decencia, franqueza, aplicadas a la empresa cooperativas en las finanzas, los precios, las pesas y medidas, la verdad en los informes y estados financieros.</span></span></li>
            <li><span><span style="font-size: medium;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;Responsabilidad y vocaci&oacute;n social:</strong></span></span><span><span style="font-size: medium;">&nbsp;Valores que hacen referencia a la presencia activa de las cooperativas y sus asociados en las comunidades donde act&uacute;a con el fin de contribuir a su mejoramiento integral o desarrollo humano sostenible.</span></span></li>
            </ol>

